package model;

/**
 *
 * @author HP
 */
public class Volume {
    private String id;
    private String value;

    public Volume(String id, String value) {
        this.id = id;
        this.value = value;
    }

    public String getId() {
        return id;
    }

    public String getValue() {
        return value;
    }
}
